    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblHA.getModel();
        model.setRowCount(0);
        
        for (HomeAddressAttributes ha : HAView.getHAdet()){
           Object[] row = new Object[3];
           row[0]= ha;
           row[1] = ha.getHlname();
           row[2] = ha.getHpassport();
           
           model.addRow(row);
           
        }
        
    }
    
