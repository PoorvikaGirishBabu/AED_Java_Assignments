    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        
        int SelectedRowIndex = tblHA.getSelectedRow();
        
        if (SelectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to View");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblHA.getModel();
        HomeAddressAttributes selectedHA =   (HomeAddressAttributes)model.getValueAt(SelectedRowIndex, 0);
        
        txthfname.setText(selectedHA.getHfname());
        txthlname.setText(selectedHA.getHlname());
        txthpassport.setText(selectedHA.getHpassport());
        txthHA1.setText(selectedHA.gethHA1());
        txthHA2.setText(selectedHA.gethHA2());
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int SelectedRowIndex = tblHA.getSelectedRow();
        
        if (SelectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this,"This HA detail is deleted");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblHA.getModel();
        HomeAddressAttributes selectedHA =   (HomeAddressAttributes)model.getValueAt(SelectedRowIndex, 0);
        
        HAView.deleteHA(selectedHA);
        
        JOptionPane.showMessageDialog(this, "Person Record Deleted");
        
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed


