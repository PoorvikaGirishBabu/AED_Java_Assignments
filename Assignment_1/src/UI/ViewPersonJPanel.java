    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int SelectedRowIndex = tblPerson.getSelectedRow();
        
        if (SelectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblPerson.getModel();
        PersonAttributes selectedPerson =   (PersonAttributes)model.getValueAt(SelectedRowIndex, 0);
        
        PAView.deletePerson(selectedPerson);
        
        JOptionPane.showMessageDialog(this, "Person Record Deleted");
        
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        
        int SelectedRowIndex = tblPerson.getSelectedRow();
        
        if (SelectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to View");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblPerson.getModel();
        PersonAttributes selectedPerson =   (PersonAttributes)model.getValueAt(SelectedRowIndex, 0);
        
        txtfname.setText(selectedPerson.getFname());
        txtlname.setText(selectedPerson.getLname());
        txtPassport.setText(selectedPerson.getPassport());
        txtPhone.setText(selectedPerson.getPhone());
        txtAddress.setText(selectedPerson.getAddress());
        
        
    }//GEN-LAST:event_btnViewActionPerformed

