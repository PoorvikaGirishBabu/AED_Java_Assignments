    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblPerson.getModel();
        model.setRowCount(0);
        
        for (PersonAttributes pa : PAView.getPersondet()){
           Object[] row = new Object[3];
           row[0]= pa;
           row[1] = pa.getLname();
           row[2] = pa.getPassport();
           
           model.addRow(row);
           
        }
        
    }

    
